# Арифметические операции

a = 12.454253
b = 8
c = a+b # "+" "-" "*" "/" (просходит в вещественных числах)
x = a // b # Деление в целых числах
t = a % b # остаток от деления
y = a ** b # возведение в степень
q = round(a * b, 3) # работа с вещественными числами, после запятой идет число символов после запятой)
a +=3 # сокращенное арифметическое действие
print (c)
print (x)
print(t)
print (y)
print (q)
print (a)